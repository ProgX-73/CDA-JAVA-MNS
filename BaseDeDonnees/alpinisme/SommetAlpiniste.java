package alpinisme;

public class SommetAlpiniste {

	private Alpiniste alpiniste;
	private Sommets sommet;
	private int annee;
	public Alpiniste getAlpiniste() {
		return alpiniste;
	}
	public void setAlpiniste(Alpiniste alpiniste) {
		this.alpiniste = alpiniste;
	}
	public Sommets getSommet() {
		return sommet;
	}
	public void setSommet(Sommets sommet) {
		this.sommet = sommet;
	}
	public int getAnnee() {
		return annee;
	}
	public void setAnnee(int annee) {
		this.annee = annee;
	}
	
	
}
